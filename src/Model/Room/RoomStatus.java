package Model.Room;

/**
 * RoomStatus is an enumeration that specifies the possible status of a room.
 * @author yijie
 *
 */
public enum RoomStatus {
	/**
	 * Room is vacant.
	 */
	Vacant,
	/**
	 * Room is occupied.
	 */
	Occupied,
	/**
	 * Room is under maintenance.
	 */
	Reserved,
	/**
	 * Allows option to exit.
	 */
	Under_Maintenance
}
